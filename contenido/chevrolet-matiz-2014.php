<h2 class="textleft width-size" >Chevrolet Matiz 2014</h2>
<figure class="fleft">
	<img class="auto-img" src="img/brand-new-cars/chevrolet/matiz-2014/matiz-2014-galeria-2.jpg" alt="Chevrolet Matiz 2014 Frente">
</figure>
<p>El <strong>Matiz 2014</strong> es un auto que desde que llego  a México con la marca Pontiac ha encantado el corazón de cada uno de los conductores, gracias a su precio accesible y su  gran rendimiento combustible, conviertiendose en una excelente  opción de confianza para toda la gente que necesita de un auto propio y que no le represente un gran gasto de manutención y cuidado del mismo.
</p>



<figure class="fleft">
	<img class="auto-img" src="img/brand-new-cars/chevrolet/matiz-2014/matiz-2014-galeria-3.jpg" alt="Chevrolet Matiz 2014 Espalda">
</figure>

<p>Ahora en este año 2014, <strong>Chevrolet</strong> lanza nuevamente a su guerrero de mil batallas, el <strong>Matiz 2014</strong>, manteniendo su línea clásica e agregandole cuatro nuevos colores en su exterior, con un interior que tiene solamente con lo necesario para circular por la ciudad y con el tamaño ideal que se necesita en estos días para transportarse a cualquier lugar.</p>

<p><strong><strong>¿Qué hay de nuevo dentro del Matiz 2014?</strong></strong></p>


<p>No cabe duda que la palabra práctico, le queda como anillo al dedo a este auto compacto, es por eso que en su interior encontrarás radio AM/FM con reproductor de CD/MP3 que te permitirá llevar tu música favorita en todo momento, aire a condicionado para que disfrutes de una temperatura agradable y desempañador eléctrico para mantener siempre tu visión clara.	
</p>


<figure class="fleft">
	<img class="auto-img" src="img/brand-new-cars/chevrolet/matiz-2014/matiz-2014-tablero.jpg" alt="Chevrolet Matiz Tablero">
</figure>

<p>	Además, siempre contaras con  el espacio necesario para llevar lo que quieras, por medio de sus asientos traseros abatibles 60/40 y su versátil cajuela con apertura desde el interior, sus vestiduras se encuentran disponibles en tela y vienen en color gris.</p>

<p><strong>¿Cuáles son los nuevos detalles y colores del Matiz 2014?</strong></p>
<figure class="fleft">
	<img class="auto-img" src="img/brand-new-cars/chevrolet/matiz-2014/matiz-2014.jpg" alt="Chevrolet Matiz 2014 de lado">
</figure>

<p>Su diseño exterior mantiene su estilo clásico y te ofrece cuatro nuevos colores para que elijas el que más te gusté: amarillo miel, azul claro, rojo flama y verde lima, incluyendo ya el clásico blanco, plata brillante y azul metálico, también podrás contar con lo necesario para cada viaje con faros de halógeno frontales, rines de acero de 13 pulgadas y espejos exteriores plegables en color negro.</p>


<figure class="fleft">
	<img class="auto-img" src="img/brand-new-cars/chevrolet/matiz-2014/desempeno.jpg" alt="Desempeño del Chevrolet Matiz 2014">
</figure>
<p><strong>¿Y en cuanto a seguridad que tiene el Matiz 2014?</strong></p>

<p>Chevrolet Matiz 2014 dispone de un motor de 1.0 litros con 4 cilindros que le permite alcanzar los 65 hp @ 5,400 rpm, con un torque de 67 lb-pie a @4,200 rpm, con transmisión manual de 5 velocidades, suspensión delantera McPherson para mayor estabilidad, todo esto en conjunto es lo que te da un gran rendimiento de combustible.
</p>

